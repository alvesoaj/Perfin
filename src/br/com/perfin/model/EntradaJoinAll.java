package br.com.perfin.model;

import java.util.Date;


public class EntradaJoinAll {
	
	private int id = 0;
	private Date Data;
	private Entrada entrada;
	private String type;
	
	public EntradaJoinAll(int id, Date data, Entrada entrada, String tipo) {		
		super();
		this.id = id;
		this.Data = data;
		this.entrada = entrada;
		this.type = tipo;
	}
	
	public EntradaJoinAll() {
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	public Date getData() {
		return Data;
	}

	public void setData(Date data) {
		Data = data;
	}

	public Entrada getEntrada() {
		return entrada;
	}
	
	public void setEntrada(Entrada entrada) {
		this.entrada = entrada;
	}
	
	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
	
}
